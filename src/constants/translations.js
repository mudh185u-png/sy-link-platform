export const getLocalizedTitle = (title, currentLang) => {
    if (!title) return title;

    const platformKeys = [
        'youtube', 'instagram', 'twitter', 'linkedin', 'github',
        'facebook', 'whatsapp', 'discord', 'twitch', 'reddit',
        'pinterest', 'steam', 'xbox', 'playstation', 'kick',
        'threads', 'tiktok', 'snapchat', 'telegram', 'global', 'email'
    ];

    for (const key of platformKeys) {
        if (translations.en[key]?.toLowerCase() === title.toLowerCase() ||
            translations.ar[key]?.toLowerCase() === title.toLowerCase()) {
            return translations[currentLang][key];
        }
    }
    return title;
};
